fn main() {
    let mut number = 0;
    let ten_x_developer = 6969;

    loop {
        if number == ten_x_developer {
            println!("The number is: {number}.");
            println!("Officially a 10x Developer. Prime would be proud.");
            break;
        }

        println!("The number is: {number}.");
        number += 1;
    }
}
